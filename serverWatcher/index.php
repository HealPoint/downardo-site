<h3><strong>Installation</strong></h3>
<p>
1. Upload all file from the download (exc. crawler.php) to your web-server where you are going to host your ServerWatcher Page.<br/>
2. Upload the crawler.php to the server that you want to monitor.<br/>
3. Edit the  config.ini File in the classes folder.<br/>
4. Change the verficationCode to your own one and change it in all uploaded crawler.php - Files.<br/>
5. Done<br/>
